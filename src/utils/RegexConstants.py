import re

class RegexConstants:
    """
    常用正则表达式常量工具类
    包含：数字、字符、日期、生活、互联网及路径等校验
    """

    # ==================== 数字校验 ====================
    # 数字
    DIGITS = r"^[0-9]*$"
    # n位数字
    N_DIGITS = r"^\d{n}$"
    # 至少n位数字
    AT_LEAST_N_DIGITS = r"^\d{n,}$"
    # m-n位数字
    M_TO_N_DIGITS = r"^\d{m,n}$"
    # 整数（非0开头，包括正负整数）
    INTEGER = r"^(-?[1-9]\d*)$"
    # 正整数
    POSITIVE_INTEGER = r"^[1-9]\d*$"
    # 负整数
    NEGATIVE_INTEGER = r"^-[1-9]\d*$"
    # 非负整数（正整数 + 0）
    NON_NEGATIVE_INTEGER = r"^(([1-9]\d*)|0)$"
    # 非正整数（负整数 + 0）
    NON_POSITIVE_INTEGER = r"^((-[1-9]\d*)|0)$"
    # 浮点数
    FLOAT = r"^-?(?:[1-9]\d*\.\d*|0\.\d*[1-9]\d*|0\.0+|0)$"
    # 正浮点数
    POSITIVE_FLOAT = r"^(?:[1-9]\d*\.\d*|0\.\d*[1-9]\d*)$"
    # 负浮点数
    NEGATIVE_FLOAT = r"^-(?:[1-9]\d*\.\d*|0\.\d*[1-9]\d*)$"
    # 非正浮点数
    NON_POSITIVE_FLOAT = r"^(?:-(?:[1-9]\d*\.\d+|0\.\d*[1-9]\d*)|0\.0+|0)$"
    # 非负浮点数
    NON_NEGATIVE_FLOAT = r"^(?:[1-9]\d*\.\d+|0\.\d+|0\.0+|0)$"
    # 仅一位小数
    ONE_DECIMAL_PLACE = r"^-?(?:0|[1-9][0-9]*)\.[0-9]{1}$"
    # 最少一位小数
    AT_LEAST_ONE_DECIMAL = r"^-?(?:0|[1-9][0-9]*)\.[0-9]{1,}$"
    # 最多两位小数
    UP_TO_TWO_DECIMALS = r"^-?(?:0|[1-9][0-9]*)\.[0-9]{1,2}$"
    # 连续重复数字（如111）
    REPEATED_DIGITS = r"^(\d)\1+$"

    # ==================== 字符校验 ====================
    # 中文
    CHINESE_CHARS = r"^[\u4E00-\u9FA5]+$"
    # 全角字符
    FULL_WIDTH_CHARS = r"^[\uFF00-\uFFFF]+$"
    # 半角字符
    HALF_WIDTH_CHARS = r"^[\u0000-\u00FF]+$"
    # 大写英文字符串
    UPPERCASE_ENGLISH = r"^[A-Z]+$"
    # 小写英文字符串
    LOWERCASE_ENGLISH = r"^[a-z]+$"
    # 英文字符串（不区分大小写）
    ENGLISH_CHARS = r"^[A-Za-z]+$"
    # 中文和数字
    CHINESE_AND_DIGITS = r"^(?:[\u4E00-\u9FA5]{0,}|\d)+$"
    # 英文和数字
    ENGLISH_AND_DIGITS = r"^[A-Za-z0-9]+$"
    # 数字、英文、下划线
    WORD_CHARS = r"^\w+$"
    # 中文、英文、数字、下划线
    CHINESE_WORD_CHARS = r"^[\u4E00-\u9FA5\w]+$"
    # 不含字母的字符串
    NO_LETTERS = r"^[^A-Za-z]*$"
    # 连续重复字符串（如aa, bb）
    REPEATED_STRINGS = r"^(.)\1+$"
    # 长度为n的字符串
    STRING_LEN_N = r"^.{n}$"
    # 可打印的ASCII字符
    ASCII_CHARS = r"^[ -~]$"

    # ==================== 日期时间 ====================
    # 日期（弱校验）
    DATE_WEAK = r"^\d{1,4}-(?:1[0-2]|0?[1-9])-(?:0?[1-9]|[1-2]\d|30|31)$"
    # 日期（严格校验，含平闰年）
    DATE_STRICT = r"^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)-02-29)$"
    # 时间（12小时制）
    TIME_12H = r"^(?:1[0-2]|0?[1-9]):[0-5]\d:[0-5]\d$"
    # 时间（24小时制）
    TIME_24H = r"^(?:[01]\d|2[0-3]):[0-5]\d:[0-5]\d$"
    # 日期+时间
    DATE_TIME = r"^(\d{1,4}-(?:1[0-2]|0?[1-9])-(?:0?[1-9]|[1-2]\d|30|31)) ((?:[01]\d|2[0-3]):[0-5]\d:[0-5]\d)$"

    # ==================== 日常生活 ====================
    # 中文名
    CHINESE_NAME = r"^[\u4E00-\u9FA5·]{2,16}$"
    # 英文名
    ENGLISH_NAME = r"^[a-zA-Z][a-zA-Z\s]{0,20}[a-zA-Z]$"
    # 车牌号（含新能源）
    LICENSE_PLATE = r"^[京津沪渝冀豫云辽黑湘皖鲁新苏浙赣鄂桂甘晋蒙陕吉闽贵粤青藏川宁琼使领][A-HJ-NP-Z](?:(?:[A-HJ-NP-Z0-9]{4}[A-HJ-NP-Z0-9挂学警港澳])|(?:(?:\d{5}[A-HJK])|(?:[A-HJK][A-HJ-NP-Z0-9][0-9]{4})))$"
    # 火车车次
    TRAIN_NUMBER = r"^[GCDZTSPKXLY1-9]\d{1,4}$"
    # 手机号（严格）
    MOBILE_PHONE = r"^(?:(?:\+|00)86)?1(?:(?:3[\d])|(?:4[5-79])|(?:5[0-35-9])|(?:6[5-7])|(?:7[0-8])|(?:8[\d])|(?:9[189]))\d{8}$"
    # 固定电话
    LANDLINE_PHONE = r"^(?:(?:\d{3}-)?\d{8}|^(?:\d{4}-)?\d{7,8})(?:-\d+)?$"
    # 身份证号码（18位）
    ID_CARD_18 = r"^[1-9]\d{5}(?:18|19|20)\d{2}(?:0[1-9]|10|11|12)(?:0[1-9]|[1-2]\d|30|31)\d{3}[0-9Xx]$"
    # 邮政编码
    POSTAL_CODE = r"^(?:0[1-7]|1[0-356]|2[0-7]|3[0-6]|4[0-7]|5[1-7]|6[1-7]|7[0-5]|8[013-6])\d{4}$"
    # 统一社会信用代码
    SOCIAL_CREDIT_CODE = r"^[0-9A-HJ-NPQRTUWXY]{2}\d{6}[0-9A-HJ-NPQRTUWXY]{10}$"
    # 微信号
    WECHAT_ID = r"^[a-zA-Z][-_a-zA-Z0-9]{5,19}$"
    # 银行卡号（16或19位）
    BANK_CARD = r"^[1-9]{1}(?:\d{15}|\d{18})$"

    # ==================== 互联网相关 ====================
    # 域名
    DOMAIN = r"^[a-zA-Z0-9][-a-zA-Z0-9]{0,62}(?:\.[a-zA-Z0-9][-a-zA-Z0-9]{0,62})+$"
    # URL（标准）
    URL = r"^https?:\/\/(?:www\.)?[-a-zA-Z0-9@:%._\+~#=]{1,256}\.[a-zA-Z0-9()]{1,6}\b(?:[-a-zA-Z0-9()!@:%_\+.~#?&\/\/=]*)$"
    # 电子邮件
    EMAIL = r"^[A-Za-z0-9\u4e00-\u9fa5]+@[a-zA-Z0-9][-a-zA-Z0-9]{0,62}(?:\.[a-zA-Z0-9][-a-zA-Z0-9]{0,62})+$"
    # 强密码（含大小写数字特符）
    PASSWORD_STRONG = r"^.*(?=.{6,})(?=.*\d)(?=.*[A-Z])(?=.*[a-z])(?=.*[!@\.#$%^&*? ]).*$"
    # IPv4
    IPV4 = r"^(?:(?:\d|[1-9]\d|1\d\d|2[0-4]\d|25[0-5])\.){3}(?:\d|[1-9]\d|1\d\d|2[0-4]\d|25[0-5])$"
    # IPv6
    IPV6 = r"^(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))$"
    # MAC地址
    MAC_ADDRESS = r"^(?:(?:[a-f0-9A-F]{2}:){5}|(?:[a-f0-9A-F]{2}-){5})[a-f0-9A-F]{2}$"
    # 图片后缀
    IMAGE_EXT = r"\.(gif|png|jpg|jpeg|webp|svg|psd|bmp|tif)+"
    # 视频后缀
    VIDEO_EXT = r"\.(swf|avi|flv|mpg|rm|mov|wav|asf|3gp|mkv|rmvb|mp4)+"

    # ==================== 路径与其他 ====================
    # MD5加密格式
    MD5 = r"^(?:[a-f\d]{32}|[A-F\d]{32})$"
    # UUID
    UUID = r"^[a-f\d]{4}(?:[a-f\d]{4}-){4}[a-f\d]{12}$"
    # 16进制颜色
    HEX_COLOR = r"^#?([0-9a-fA-F]{3}|[0-9a-fA-F]{6})$"
    # Windows文件路径
    WINDOWS_FILE_PATH = r"^[a-zA-Z]:(?:\\[\w\u4E00-\u9FA5\s]+)+[.\w\u4E00-\u9FA5\s]+$"
    # Linux文件路径
    LINUX_FILE_PATH = r"^\/(?:[^/]+\/)*[^/]+$"